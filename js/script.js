function buildPortfolioGrid() {
  const pg = document.getElementById('portfolioGrid');
  for (let i = 1; i <= 12; i++) {
    const d = document.createElement('div');
    d.className = 'portfolio-item';
    d.setAttribute('data-num', String(i).padStart(2,'0'));
    d.style.cssText = `background: rgba(0,255,65,0.03); border-color: rgba(0,255,65,0.15);`;
    const img = document.createElement('img');
    img.src = `./assets/img${i}.PNG`;  // ← corrigé
    img.style.cssText = 'width:100%;height:100%;object-fit:cover;opacity:0.7;';
    img.onerror = () => { img.style.display='none'; };
    d.appendChild(img);
    pg.appendChild(d);
  }
  const fg = document.getElementById('formationsGrid');
  ['a','b','c','d','e','f'].forEach((letter) => {
    const d = document.createElement('div');
    d.className = 'portfolio-item';
    d.setAttribute('data-num', letter.toUpperCase());
    const ext = letter === 'f' ? 'jpg' : 'png';
    const img = document.createElement('img');
    img.src = `./assets/${letter}.${ext}`;
    img.style.cssText = 'width:100%;height:100%;object-fit:cover;opacity:0.7;';
    img.onerror = () => { img.style.display='none'; };
    d.appendChild(img);
    fg.appendChild(d);
  });
}
buildPortfolioGrid();

